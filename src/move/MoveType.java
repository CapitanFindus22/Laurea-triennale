package move;

/**
 * The moves can be categorized in one these five categories
 */
public enum MoveType {

	/**
	 * Normal attack
	 */
	NorAtt,
	
	/**
	 * Special attack
	 */
	SpAtt,
	
	/**
	 * Change enemy stats
	 */
	EnStats,
	
	/**
	 * Change self stats
	 */
	SelfStats,
	
	/**
	 * Moves that require a specific function
	 */
	Other
	
}
